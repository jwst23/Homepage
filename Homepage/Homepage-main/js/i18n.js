document.addEventListener("DOMContentLoaded", () => {
    // Check local storage for language preference, default to 'en'
    let currentLang = localStorage.getItem("aetheria_lang") || "en";
    applyTranslations(currentLang);

    // Add click listeners to language menu items
    const langLinks = document.querySelectorAll(".lang-menu a");
    langLinks.forEach(link => {
        link.addEventListener("click", (e) => {
            e.preventDefault();
            const selectedLang = e.target.getAttribute("data-lang");
            if (selectedLang) {
                applyTranslations(selectedLang);
                localStorage.setItem("aetheria_lang", selectedLang);
            }
        });
    });
});

function applyTranslations(lang) {
    const dict = translations[lang];
    if (!dict) return;

    // Update all elements with data-i18n attribute
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (dict[key]) {
            if (key === 'hero_title') {
                el.innerHTML = dict[key]; // Allow HTML tags for hero_title
            } else {
                el.textContent = dict[key];
            }
        }
    });

    // Update the language dropdown text itself
    const langDisplay = document.getElementById("current-lang-display");
    if (langDisplay && dict["nav_lang"]) {
        langDisplay.textContent = dict["nav_lang"];
    }
}
